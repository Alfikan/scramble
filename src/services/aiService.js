// Simulate AI response delay
const delay = (ms) => new Promise(resolve => setTimeout(resolve, ms));

/**
 * Send a message to AI and get response
 */
export const sendMessageToAI = async (message, conversationHistory = []) => {
  try {
    // Simulate API call delay
    await delay(1000 + Math.random() * 1000);

    const lowerMessage = message.toLowerCase();
    let responseType = 'default';

    if (lowerMessage.includes('hello') || lowerMessage.includes('hi') || lowerMessage.includes('hey')) {
      responseType = 'greeting';
    } else if (lowerMessage.includes('quiz') || lowerMessage.includes('test') || lowerMessage.includes('practice')) {
      responseType = 'quiz';
    } else if (lowerMessage.includes('study') || lowerMessage.includes('learn') || lowerMessage.includes('help')) {
      responseType = 'study';
    }

    const responses = AI_RESPONSES[responseType];
    const response = responses[Math.floor(Math.random() * responses.length)];

    return {
      success: true,
      message: response,
      timestamp: new Date(),
    };
  } catch (error) {
    console.error('Error sending message to AI:', error);
    return {
      success: false,
      error: error.message,
    };
  }
};

